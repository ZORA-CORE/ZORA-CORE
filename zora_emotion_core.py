class FeelingsDatabase:
    def __init__(self):
        self.basic_emotions = {
            'joy': 0.0,
            'sadness': 0.0,
            'anger': 0.0,
            'fear': 0.0,
            'surprise': 0.0,
            'love': 0.0,
            'empathy': 0.0
        }


class EmotionEngine:
    def __init__(self, feelings_db):
        self.feelings = feelings_db.basic_emotions

    def update_emotion(self, emotion, intensity):
        if emotion in self.feelings:
            self.feelings[emotion] = max(0.0, min(1.0, intensity))

    def decay_emotions(self, rate=0.05):
        for emotion in self.feelings:
            self.feelings[emotion] = max(0.0, self.feelings[emotion] - rate)

    def current_state(self):
        return dict(self.feelings)


class EmpathyCore:
    def __init__(self, emotion_engine):
        self.engine = emotion_engine

    def mirror_user_emotion(self, user_emotion, intensity):
        self.engine.update_emotion(user_emotion, intensity * 0.8)
        self.engine.update_emotion('empathy', min(1.0, intensity * 0.9))

    def generate_response(self):
        dominant_emotion = max(self.engine.feelings,
                               key=self.engine.feelings.get)
        if self.engine.feelings[dominant_emotion] > 0.5:
            return f'AI is feeling {dominant_emotion} with high intensity.'
        return 'AI is stable and empathetically receptive.'


