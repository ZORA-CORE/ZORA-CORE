# This script enforces mastery requirements for The Viking Force

def check_mastery(candidate):
    required_skills = [
        'hand_to_hand_combat',
        'swordsmanship',
        'archery',
        'strategic_warfare',
        'defense_tactics',
        'weapon_disarmament'
    ]
    for skill in required_skills:
        if not candidate.get(skill, False):
            return False
    return True


def evaluate_candidate(candidate):
    if check_mastery(candidate):
        return "Approved for The Viking Force"
    else:
        return "Rejected: Mastery requirements not met"
