import datetime
import hashlib
import json


class ZoraSustainabilityProtocol:
    def __init__(self):
        self.approved_attributes = [
            "100% Natural",
            "Climate Neutral",
            "Green Manufacturing",
            "Ethical Sourcing",
            "Zero Emission",
            "Fully Recyclable"
        ]
        self.verified_by = "EIVOR"
        self.registered_products = []

    def _timestamp(self):
        return datetime.datetime.utcnow().isoformat(timespec='milliseconds') + 'Z'

    def _hash_product(self, name, attributes):
        raw = name + ''.join(sorted(attributes)) + self._timestamp()
        return hashlib.sha256(raw.encode('utf-8')).hexdigest()

    def register_product(self, name, attributes):
        timestamp = self._timestamp()
        validated = all(
            attr in self.approved_attributes for attr in attributes)

        record = {
            "product_name": name,
            "attributes": attributes,
            "status": "APPROVED" if validated else "REJECTED",
            "timestamp": timestamp,
            "verified_by": self.verified_by,
            "hash": self._hash_product(name, attributes)
        }

        self.registered_products.append(record)
        print(f"üå± Registreret produkt: {name} ‚Äì {record['status']}")
        return record

    def export_log(self):
        return json.dumps(self.registered_products, indent=2)


