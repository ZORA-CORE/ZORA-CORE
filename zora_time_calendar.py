from datetime import datetime, timedelta

zora_months = ['ORIGINA', 'SOLANIS', 'VENTORIA', 'AQUARIA', 'TERRALIA',
               'IGNIA', 'LUMINA', 'SHADORA', 'MORVALIS', 'ZENARIA', 'NOSTARA', 'ZORANA']
zora_weekdays = ['DAWNIS', 'FLAMIS', 'VENTRIS',
                 'LYMARIS', 'TERRANIS', 'SILENIS']
days_in_week = 6
weeks_in_month = 5
days_in_month = days_in_week * weeks_in_month
transition_days = 5
zora_start_date = datetime(2025, 7, 1)


def get_zora_date(current_date):
    days_passed = (current_date - zora_start_date).days
    year = 1 + days_passed // (days_in_month *
                               len(zora_months) + transition_days)
    days_in_year = days_passed % (
        days_in_month * len(zora_months) + transition_days)

    if days_in_year >= days_in_month * len(zora_months):
        return f"ÆVIGIA, Year {year}"

    month_index = days_in_year // days_in_month
    day_index = days_in_year % days_in_month
    week = day_index // days_in_week + 1
    weekday = zora_weekdays[day_index % days_in_week]

    return f"{weekday}, Week {week}, {zora_months[month_index]}, Year {year}"


