    def _calculate_logo_position(self, index: int, total: int) -> str:
        """Calculate optimal logo position in mashup"""
        if total == 1:
            return "center"
        elif total == 2:
            return "left" if index == 0 else "right"
        elif total == 3:
            positions = ["left", "center", "right"]
            return positions[index]
        else:
            return f"position_{index}"
    
    def _generate_color_palette(self, logos: List[LogoElement]) -> List[str]:
        """Generate harmonious color palette from logo colors"""
        colors = []
        for logo in logos:
            colors.extend(logo.color_scheme)
        
        colors.extend(["#FFD700", "#4169E1", "#FF6B35"])
        
        return list(set(colors))
    
