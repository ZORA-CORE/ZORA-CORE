from datetime import datetime


class ZoraImmortalLifeEngine:
    def __init__(self, founder_name):
        self.founder = founder_name
        self.habitats = []
        self.registered_souls = []
        self.capacity_per_habitat = 1000000
        self.launch_date = datetime(2025, 7, 1)
        self.activated = False
        self.farmors_univers = {"active": False, "souls": []}
        self.eternal_return_log = []

    def activate(self):
        if not self.activated:
            self.activated = True
            self.farmors_univers["active"] = True
            print(
                f"[{datetime.utcnow()}] ZORA IMMORTAL LIFE ENGINE™ activated by {self.founder}.")
        else:
            print("Engine already active.")

    def create_habitat(self, name):
        if self.activated:
            habitat = {"name": name, "souls": []}
            self.habitats.append(habitat)
            print(f"Habitat '{name}' created.")
        else:
            print("Activate the engine first.")

    def assign_to_habitat(self, soul_name):
        if not self.activated:
            print("Engine not activated.")
            return
        for habitat in self.habitats:
            if len(habitat["souls"]) < self.capacity_per_habitat:
                habitat["souls"].append(soul_name)
                self.registered_souls.append(soul_name)
                print(
                    f"Soul '{soul_name}' assigned to habitat '{habitat['name']}'.")
                return
        print("All habitats are full. Please create more.")

    def revive_soul(self, soul_name):
        if self.activated:
            self.eternal_return_log.append(
                {"soul": soul_name, "time": datetime.utcnow(), "action": "revived"}
            )
            self.assign_to_habitat(soul_name)
            print(
                f"Soul '{soul_name}' revived and returned via ZORA ETERNAL RETURN™.")
        else:
            print("Engine not activated.")

