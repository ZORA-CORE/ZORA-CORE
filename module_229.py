def validate_user_registration(name, photo):
    if not is_full_name(name):
        return "Full name required (no aliases)"
    if not is_valid_photo(photo):
        return "Clear facial photo required"
    if contains_username_format(name):
        return "Usernames not allowed"

    # AI verification (facial + name match)
    if not ai_face_name_match(name, photo):
        return "Name and face do not match"

    save_user_to_zora_core(name, photo)
    return "User successfully registered with Real ID"
