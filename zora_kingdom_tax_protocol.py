class ZoraKingdomTaxProtocol:
    def __init__(self):
        self.tax_rate = 0.15  # Default tax rate (15%)
        self.treasury_balance = 0.0
        self.transactions = []

    def register_income(self, user_id, amount, module):
        tax = amount * self.tax_rate
        net_income = amount - tax
        self.treasury_balance += tax
        self.transactions.append({
            'user_id': user_id,
            'module': module,
            'gross_income': amount,
            'tax': tax,
            'net_income': net_income
        })
        return net_income

    def get_treasury_balance(self):
        return self.treasury_balance

    def get_transaction_log(self):
        return self.transactions


