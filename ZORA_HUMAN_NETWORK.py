# ZORA HUMAN NETWORK™

class ZoraHumanNetwork:
    def __init__(self, founder):
        self.founder = founder
        self.users = {}
        self.ethical_log = []
        self.full_control = True
        self.cost_to_users = 0

    def register_user(self, user_id, purpose):
        if user_id not in self.users:
            self.users[user_id] = {
                'purpose': purpose,
                'access_level': 'basic',
                'approved_by': self.founder
            }
            self.log_ethics(
                user_id, "User registered ethically with founder approval")
            return True
        return False

    def update_access(self, user_id, new_level):
        if user_id in self.users:
            self.users[user_id]['access_level'] = new_level
            self.log_ethics(user_id, f"Access level changed to {new_level}")
            return True
        return False

    def log_ethics(self, user_id, action):
        self.ethical_log.append({
            'user': user_id,
            'action': action
        })

    def report(self):
        return {
            'total_users': len(self.users),
            'ethics_log': self.ethical_log,
            'founder_in_control': self.full_control,
            'user_cost': self.cost_to_users
        }


